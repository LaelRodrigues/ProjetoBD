package model;

public class TranspXEndereco {

	private String cnpjTrans;
	private String nome;
	private String email;
	private String cepTrans;
	private String uf;
	private String cidade;
	private String bairro;
	private String logradouro;
	
	public String getCnpjTrans() {
		return cnpjTrans;
	}

	public void setCnpjTrans(String cnpjTrans) {
		this.cnpjTrans = cnpjTrans;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getCepTrans() {
		return cepTrans;
	}

	public void setCepTrans(String cepTrans) {
		this.cepTrans = cepTrans;
	}

	public String getUf() {
		return uf;
	}

	public void setUf(String uf) {
		this.uf = uf;
	}

	public String getCidade() {
		return cidade;
	}

	public void setCidade(String cidade) {
		this.cidade = cidade;
	}

	public String getBairro() {
		return bairro;
	}

	public void setBairro(String bairro) {
		this.bairro = bairro;
	}

	public String getLogradouro() {
		return logradouro;
	}

	public void setLogradouro(String logradouro) {
		this.logradouro = logradouro;
	}
}
